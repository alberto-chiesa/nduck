namespace NDuck.Data.Enum
{
    /// <summary>
    /// Enumerates the available categories of
    /// .NET types (Class, Enum, Interface and Struct).
    /// </summary>
    public enum ClassType
    {
        /// <summary>
        /// .NET Class
        /// </summary>
        Class,

        /// <summary>
        /// Represents an Enum type
        /// </summary>
        Enum,
        
        /// <summary>
        /// Interface Type
        /// </summary>
        Interface,

        /// <summary>
        /// Struct Type
        /// </summary>
        Struct
    }
}